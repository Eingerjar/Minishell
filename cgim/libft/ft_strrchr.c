char	*ft_strrchr(const char	*s, int c)
{
	const char	*p;

	p = 0;
	while (*s != '\0')
	{
		if (*s == c)
			p = s;
		s++;
	}
	if (*s == c)
		p = s;
	return ((char *)p);
}
