#include "minishell.h"

int	skip_quote(char *cmd, int i)
{
	int	q;

	q = 0;
	if (cmd[i] == '"')
		q = 2;
	else
		q = 1;
	i++;
	while (q != 0)
	{
		if ((cmd[i] == '"' && q == 2) || (cmd[i] == '\'' && q == 1))
			q = 0;
		i++;
	}
	return (i);
}
