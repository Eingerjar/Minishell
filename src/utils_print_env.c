#include "../includes/minishell.h"

void	print_env(void)
{
	int	i;

	i = -1;
	printf("------------------------------------\n");
	while (g_global.wel_env[++i])
		printf("[%d] %s\n", i, g_global.wel_env[i]);
	printf("------------------------------------\n");
}
